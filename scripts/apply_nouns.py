# ════════════════════════════════════════════════════════════
#  1. Wiktionary санаттары
# ════════════════════════════════════════════════════════════
WIKT_CATEGORIES = [
    "Қазақша_зат_есімдер",
    "Қазақ_тіліндегі_зат_есімдер",
    "Қазақша_жалпы_есімдер",
]

def wikt_category(cat: str) -> set[str]:
    url = "https://kk.wiktionary.org/w/api.php"
    words: set[str] = set()
    cont = None
    while True:
        params = {
            "action": "query", "list": "categorymembers",
            "cmtitle": f"Санат:{cat}", "cmlimit": "500",
            "cmnamespace": "0", "format": "json",
        }
        if cont:
            params["cmcontinue"] = cont
        try:
            r = SESSION.get(url, params=params, timeout=15)
            data = r.json()
            for m in data.get("query", {}).get("categorymembers", []):
                t = m.get("title", "").strip().lower()
                if t:
                    words.add(t)
            cont = data.get("continue", {}).get("cmcontinue")
            if not cont:
                break
            time.sleep(DELAY)
        except Exception as e:
            print(f"   ⚠️  Wiktionary [{cat}]: {e}")
            break
    return words


def scrape_wiktionary() -> set[str]:
    print("📖 Wiktionary санаттары...")
    all_words: set[str] = set()
    for cat in WIKT_CATEGORIES:
        w = wikt_category(cat)
        print(f"   [{cat}] → {len(w)}")
        all_words |= w
        time.sleep(DELAY)
    return all_words


# ════════════════════════════════════════════════════════════
#  2. Wikipedia AllPages — бір сөзді кирилл тақырыптар
# ════════════════════════════════════════════════════════════
def scrape_wikipedia_allpages() -> set[str]:
    print("🌐 Wikipedia AllPages...")
    url = "https://kk.wikipedia.org/w/api.php"
    words: set[str] = set()
    cont = None
    fetched = 0

    while fetched < 15000:
        params = {
            "action": "query", "list": "allpages",
            "aplimit": "500", "apnamespace": "0",
            "apminsize": "200", "format": "json",
        }
        if cont:
            params["apcontinue"] = cont
        try:
            r = SESSION.get(url, params=params, timeout=15)
            data = r.json()
            for p in data.get("query", {}).get("allpages", []):
                title = p.get("title", "").strip().lower()
                # тек бір сөзді, кирилл, 3-20 әріп
                if re.match(r'^[а-яёәіңғүұқөһ]{3,20}$', title):
                    words.add(title)
            fetched += 500
            cont = data.get("continue", {}).get("apcontinue")
            if not cont:
                break
            time.sleep(DELAY)
        except Exception as e:
            print(f"   ⚠️  Wikipedia: {e}")
            break

    print(f"   Wikipedia → {len(words)} сөз")
    return words


# ════════════════════════════════════════════════════════════
#  3. sozdik.kz
# ════════════════════════════════════════════════════════════
def scrape_sozdik() -> set[str]:
    print("📚 sozdik.kz...")
    words: set[str] = set()
    letters = list("аәбвгғдежзийкқлмнңоөпрстуүұфхһшщыіэюя")

    for letter in letters:
        try:
            url = f"https://sozdik.kz/kk/dictionary/translate/kk/ru/{letter}/"
            r = SESSION.get(url, timeout=12)
            soup = BeautifulSoup(r.text, "html.parser")
            for a in soup.find_all("a", href=True):
                if "/translate/kk/" in a["href"]:
                    t = a.get_text(strip=True).lower()
                    if 2 <= len(t) <= 25:
                        words.add(t)
            time.sleep(DELAY)
        except Exception:
            pass

    print(f"   sozdik.kz → {len(words)} сөз")
    return words


# ════════════════════════════════════════════════════════════
#  Сүзгі
# ════════════════════════════════════════════════════════════
def is_kazakh_cyrillic(word: str) -> bool:
    return bool(re.match(r'^[а-яёәіңғүұқөһ\-]{2,30}$', word.strip().lower()))

def clean_set(raw: set[str]) -> set[str]:
    result = set()
    for w in raw:
        w = w.strip().lower()
        if " " in w or "\t" in w:
            continue
        if is_kazakh_cyrillic(w):
            result.add(w)
    return result


