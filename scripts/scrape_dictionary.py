BAD = set('abcdefghijklmnopqrstuvwxyz0123456789@#$%^&*()[]{}<>/\\\'",.:;!?-_')

def clean(words):
    result = set()
    for w in words:
        w = w.lower().strip().strip('«»()[].,;:!?-_')
        if 2 <= len(w) <= 25 and not any(c in BAD for c in w) and ' ' not in w:
            result.add(w)
    return result


def filter_vectors(word_set):
    if not VECTORS_PATH.exists():
        print(f"\n❌ {VECTORS_PATH} жоқ!")
        return
    print(f"\n💾 Векторлар сүзгіленуде ({len(word_set):,} сөз)...")
    matched = {}
    with open(VECTORS_PATH, encoding="utf-8") as f:
        header = f.readline().strip()
        _, dim = header.split()
        for line in f:
            w = line.split(" ", 1)[0].lower()
            if w in word_set and w not in matched:
                parts = line.rstrip().split(" ")
                parts[0] = w
                matched[w] = " ".join(parts)
    print(f"   Табылды: {len(matched):,} / {len(word_set):,}")
    OUTPUT_VEC.parent.mkdir(exist_ok=True)
    with open(OUTPUT_VEC, "w", encoding="utf-8") as f:
        f.write(f"{len(matched)} {dim}\n")
        for line in matched.values():
            f.write(line + "\n")
    print(f"   ✅ {OUTPUT_VEC} жазылды!")


def main():
    print("🇰🇿 Қазақша ЗАТ ЕСІМДЕР тізімін жасау")
    print("="*50)

    # Барлық санаттардан жинау
    all_nouns = set()
    for category, words_str in NOUNS.items():
        words = clean(words_str.split())
        all_nouns |= words
        print(f"  {category:25s}: {len(words):4d} сөз")

    # words.py-дағы сөздерді де қосу
    try:
        from words import DAILY_WORDS
        daily = clean(DAILY_WORDS)
        before = len(all_nouns)
        all_nouns |= daily
        print(f"\n  words.py: +{len(all_nouns)-before} сөз қосылды")
    except Exception as e:
        print(f"\n  ⚠️ words.py: {e}")

    print(f"\n{'='*50}")
    print(f"📊 Жиынтық ЗАТ ЕСІМДЕР: {len(all_nouns):,}")

    # Сақтау
    OUTPUT_WORDS.parent.mkdir(exist_ok=True)
    sorted_nouns = sorted(all_nouns)
    with open(OUTPUT_WORDS, "w", encoding="utf-8") as f:
        for w in sorted_nouns:
            f.write(w + "\n")
    print(f"💾 {OUTPUT_WORDS}: {len(sorted_nouns)} сөз сақталды")

    # Векторларға сүзгі
    filter_vectors(all_nouns)

    print(f"\n📌 Келесі қадам — embeddings.py ішінде:")
    print(f"   VECTORS_PATH = Path('./data/wiki.kk.nouns.vec')")
    print(f"   CACHE_PATH   = Path('./data/kk_nouns_cache.npz')")

    # Тізімді көрсету
    print(f"\n📝 Алғашқы 20 сөз:")
    for w in sorted_nouns[:20]:
        print(f"   {w}")


if __name__ == "__main__":
    main()