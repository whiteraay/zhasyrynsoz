def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--target",    type=int,   default=5000)
    ap.add_argument("--neighbors", type=int,   default=30)
    ap.add_argument("--min-sim",   type=float, default=0.50,
                    help="Минималды косинус ұқсастығы (default: 0.50)")
    ap.add_argument("--dry-run",   action="store_true")
    args = ap.parse_args()

    # ── Seed ──────────────────────────────────────────────────
    seed  = set()
    w2cat = {}
    for cat, words in SEED_BY_CAT.items():
        for w in words:
            w = w.strip().lower()
            if KAZ.match(w):
                bad, _ = is_bad_word(w)
                if not bad:
                    seed.add(w)
                    w2cat[w] = cat

    print(f"✅ Seed: {len(seed):,} сөз  ({len(SEED_BY_CAT)} санат)")

    # ── FastText ───────────────────────────────────────────────
    all_w, vecs, w2i, np = load_fasttext()

    miss = seed - set(w2i)
    if miss:
        print(f"ℹ️  {len(miss)} seed-сөз FastText-та жоқ — алынды")
        seed -= miss
    print(f"   FastText-та бар seed: {len(seed):,}")

    # ── Кеңейту ────────────────────────────────────────────────
    final    = set(seed)
    rejected = []
    frontier = list(seed)

    print(f"\n🔍 {args.target:,} сөзге дейін кеңейту "
          f"(sim≥{args.min_sim}, {args.neighbors} көрші)...\n")

    iteration = 0
    while len(final) < args.target and frontier:
        iteration += 1
        new_f = []
        for sw in frontier:
            if len(final) >= args.target:
                break
            for nb, sim in get_neighbors(sw, all_w, vecs, w2i, np,
                                         args.neighbors):
                if nb in final:
                    continue

                # ① Жақындық тексерісі
                if sim < args.min_sim:
                    rejected.append((nb, f"sim={sim:.2f}"))
                    continue

                # ② Жалғаулы форма тексерісі
                if is_inflected(nb, final):
                    rejected.append((nb, "жалғаулы"))
                    continue

                # ③ Жарамсыз сөз тексерісі (жаңа!)
                bad, reason = is_bad_word(nb)
                if bad:
                    rejected.append((nb, reason))
                    continue

                final.add(nb)
                w2cat.setdefault(nb, "Басқа")
                new_f.append(nb)

        frontier = new_f
        print(f"   Итерация {iteration}: {len(final):,} сөз  (+{len(new_f)})")
        if not new_f:
            break

    # ── 2-ші тазалау ──────────────────────────────────────────
    print("\n🧹 Соңғы тазалау...")
    before = len(final)
    final  = {w for w in final
              if not is_inflected(w, final - {w}) and not is_bad_word(w)[0]}
    print(f"   {before - len(final)} алынды → {len(final):,} таза")

    # ── Статистика ─────────────────────────────────────────────
    by_cat = {}
    for w in final:
        by_cat.setdefault(w2cat.get(w, "Басқа"), []).append(w)

    rej_inf  = sum(1 for _, r in rejected if "жалғаулы" in r)
    rej_bad  = sum(1 for _, r in rejected
                   if any(x in r for x in ["proper","compound","blocklist",
                                           "non_kaz","too_long"]))
    rej_sim  = sum(1 for _, r in rejected if "sim=" in r)

    print(f"\n{'═'*60}")
    print(f"✅ НӘТИЖЕ: {len(final):,} таза түбір сөз")
    print(f"   Seed: {len(seed):,}  |  Жаңа: {len(final)-len(seed):,}")
    print(f"   Жалғаулы алынды:  {rej_inf:,}")
    print(f"   Жарамсыз алынды:  {rej_bad:,}  (жалқы есім, қосма сөз т.б.)")
    print(f"   Жақындық төмен:   {rej_sim:,}")
    print(f"{'─'*60}")
    for cat in sorted(by_cat, key=lambda c: -len(by_cat[c])):
        cnt = len(by_cat[cat])
        bar = "█" * min(cnt // 5, 42)
        print(f"   {cat:25s} {cnt:4d}  {bar}")
    print(f"{'═'*60}")

    if args.dry_run:
        print("ℹ️  --dry-run: жазылмады")
        return

    # ── Жазу ──────────────────────────────────────────────────
    final_list = sorted(final)
    OUT_NOUNS.parent.mkdir(exist_ok=True)
    OUT_NOUNS.write_text("\n".join(final_list) + "\n", encoding="utf-8")

    with open(REJECTED_LOG, "w", encoding="utf-8") as f:
        for label, tag in [("Жалғаулы","жалғаулы"),
                            ("Жарамсыз","proper"),("Жарамсыз","compound"),
                            ("Жарамсыз","blocklist"),("Жарамсыз","non_kaz"),
                            ("Жарамсыз","too_long")]:
            subset = [w for w, r in rejected if tag in r]
            if subset:
                f.write(f"# {label} — {tag} ({len(subset)})\n")
                for w in sorted(subset)[:100]:
                    f.write(w + "\n")
                f.write("\n")

    known  = sorted(c for c in by_cat if c != "Басқа")
    lines  = []
    for cat in known + (["Басқа"] if "Басқа" in by_cat else []):
        ws  = sorted(by_cat[cat])
        sep = "─" * max(1, 44 - len(cat))
        lines.append(f"    # ── {cat} {sep}")
        for i in range(0, len(ws), 8):
            chunk = ws[i:i + 8]
            lines.append("    " + ", ".join(f'"{w}"' for w in chunk) + ",")
        lines.append("")

    content = TEMPLATE.format(
        date=datetime.today().strftime("%Y-%m-%d"),
        n=len(final_list),
        blocks="\n".join(lines),
    )

    if WORDS_PY.exists():
        WORDS_PY.with_suffix(".py.bak").write_bytes(WORDS_PY.read_bytes())
    WORDS_PY.parent.mkdir(exist_ok=True)
    WORDS_PY.write_text(content, encoding="utf-8")

    print(f"\n💾 {OUT_NOUNS}: {len(final_list):,} сөз")
    print(f"✅ {WORDS_PY}: {len(final_list):,} сөз")
    print(f"\nТексеру:  python3 scripts/curate_words.py stats")
    print(f"Журнал:   cat {REJECTED_LOG} | head -40")


if __name__ == "__main__":
    main()
